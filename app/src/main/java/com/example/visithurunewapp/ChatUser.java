package com.example.visithurunewapp;

public class ChatUser {
    private String emailW;
    private String passwordW;
    private String userNameW;
    private String statusW;

    public ChatUser() {
    }

    public String getEmailW() {
        return emailW;
    }

    public void setEmailW(String emailW) {
        this.emailW = emailW;
    }

    public String getPasswordW() {
        return passwordW;
    }

    public void setPasswordW(String passwordW) {
        this.passwordW = passwordW;
    }

    public String getUserNameW() {
        return userNameW;
    }

    public void setUserNameW(String userNameW) {
        this.userNameW = userNameW;
    }

    public String getStatusW() {
        return statusW;
    }

    public void setStatusW(String statusW) {
        this.statusW = statusW;
    }
}
